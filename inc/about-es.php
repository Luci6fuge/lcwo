<h2>Compatibilidad del navegador, material t&eacute;cnico</h2>

<p>LCWO es compatible con los navegadores m&aacute;s modernos, en particular las &uacute;ltimas dos generaciones de <a href="http://www.firefox.com/">Mozilla Firefox</a> y Microsoft Internet Explorer, pero tambi&eacute;n se ha informado que funcionan con <a href="http://www.opera.com/">Opera</a> y Apple's <a href="http://www.apple.com/safari/">Safari</a>.</p>

<p>Para disfrutar de todas las caracter&iacute;sticas de LCWO, JavaScript
debe estar habilitado en un navegador compatible con <a href="http://en.wikipedia.org/wiki/HTML5">HTML5</a>
o utilizar Adobe <a href="http://www.adobe.com/de/products/flashplayer/">Flash Player</a>
para la reproducci&oacute;n de CW es m&aacute;s recomendable.</p>

<p>El desarrollo de LCWO se lleva a cabo en <a
href="http://www.debian.org/">Debian GNU/Linux</a> y <a href="http://www.ubuntu.com">Ubuntu</a>,
utilizando el fant&aacute;stico editor <a href="http://www.vim.org/">vim</a>. El sitio se ejecuta en un
servidor web <a href="http://www.apache.org/">Apache</a>, es en su mayor&iacute;a programado en
<a href="http://www.php.net/">PHP</a> (sin frameworks, todo el homebrew) y utiliza una base de
datos <a href="http://www.mysql.com/">MySQL</a>. Los archivos Morse MP3 y OGG son generados por
versiones personalizadas de <a href="http://fkurz.net/ham/ebook2cw.html">ebook2cw</a> que funcionan
con CGIs compiladas, escritas en C.</p>
