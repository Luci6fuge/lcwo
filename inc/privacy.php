is saved and the visibility. <strong>The data is collected for the sole purpose
of making the user experience of the Morse trainer as comfortable as possible.</strong>
</p>
<ol>
    <li><strong>Creating an account:</strong> Username (can be an anonymous
        pseudonym), name (voluntary), password (encrypted), location
        (voluntary), email address (voluntary), language, closest server (EU or
        NA), Date. <br>
        <strong>Data storage duration:</strong> Until user changes it or
        deletes their account. <br>
        <strong>Visibility:</strong> Member list, user profile (without email
        and password)</li>
    <li><strong>On each login:</strong> Account and time of the login.<br>
        <strong>Data storage duration:</strong> 10 minutes after the last
        activity on the site.<br>
        <strong>Visibility:</strong> Shown on the "Who is online?" tab.</li>
    <li><strong>When finishing a practice attempt:</strong> Type of the attempt, 
        speed, number of errors or points, date.<br>
        <strong>Data storage duration:</strong> Until deleted by user or
        deletion of account.<br>
        <strong>Visibility:</strong> Fully visible only to the user. High score
        list shows top results. After opt-in also partly visible on the user
        profile page.</li>
    <li><strong>Forum posts:</strong> Forum postings are published after review
        by the site staff.<br>
        <strong>Data storage duration:</strong> Until the user deletes it or
        the account is terminated.<br>
        <strong>Visibility:</strong> Public (in the main forum and public user
        groups; otherwise only for members of the user group).</li>
    <li><strong>Private messages:</strong> PMs are sent directly to the inbox
        of another user.<br>
        <strong>Data storage duration:</strong> Until the receiver deletes
        them, or the sender's account is terminated.
        <br>
        <strong>Visibility:</strong> For the receiver only.</li>
    <li><strong>User profile:</strong> User created profile text and optionally
        avatar picture.<br>
        <strong>Data storage duration:</strong> Until the user changes it or 
        account termination.
        <br>
        <strong>Visibility:</strong> Public (no login required).</li>
    <li><strong>Any other data:</strong> Any other data not mentioned is always
        handled with the utmost care and by the priciple of minimizing
        data.</li>
</ol>

